public class first{
    public static void main(String [] args){

       int a=20;
       int b=11;

       //Addition
       int sum = a+b;
       System.out.println(sum);

       //subtraction
       int sub =a-b;
       System.out.println(sub);
        
       //multiplication
       int mul =a*b;
       System.out.println(mul);

       //Division
       int div = a/b;
       System.out.println(div);

       //Reminder
       int rem=a%b;
       System.out.println(rem);


       //Increment
       int x=5;
       x++; // x=x+1;
       System.out.println(x);

       //decrement
       int y=5;
       y--; //y=y-1;
       System.out.println(y);
       
    }
}